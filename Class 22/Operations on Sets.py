# Operations on Sets

# set with intergers

s1 = { 12,34,56,73,24,56}
print(s1)

# set with mixed data
s2 = {34,67,90,87,89,'cut',True,(12,45,67)}
print(s2)

# set with duplicates
s3 = {3,4,5,5,5,3,2,1}
print(s3)

# add element
s3.add(100)
print(s3)
s4 = set ([23,45,67,67,89])
print(s4)
s4.pop()
print(s4)