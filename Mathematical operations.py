# Mathamatical Operation

import math

print("The ceil value of 67.89 is:", math.ceil(67.89))
print("The floor value of 67.89 is:", math.floor(67.89))

x = 90
y = -76

print("the x value before copying the sign is", x)

print("the x value after copying the sign is", math.copysign(x, y))

print("the absolute value of -89 is", math.fabs(-89))

print("the gcd of 45,15 is", math.gcd(45,15))
